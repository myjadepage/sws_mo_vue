  6.    회사는 다음과 같은 사유가 있는 경우 회원가입을 유보할 수 있습니다. 이 때, 회사는 이용신청자에게 승낙유보의 사유, 승낙 가능 시기 또는 승낙에 필요한 추가 요청정보나 자료 등 승낙유보와 관련된 사항을 해당 서비스 화면에 게시하거나 전화, 문자, e-mail 등의 방법으로 통지합니다.
