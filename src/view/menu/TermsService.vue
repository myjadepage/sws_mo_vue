제8조 (개인정보의 변경)
1.	회원은 이용신청 시 허위의 정보를 제공하여서는 아니 되며, 기재한 사항이 변경되었을 경우에는 즉시 변경사항을 최신의 정보로 수정하여야 합니다. 
2	회사의 회원에 대한 통지는 회원의 접속정보에 도달함으로써 통지된 것으로 보며, 수정하지 않은 정보로 인하여 발생하는 손해는 당해 회원이 전적으로 부담하며, 회사는 이에 대하여 아무런 책임을 지지 않습니다. 
3	거래가 성사되었을 때 회사는 판매자와 회원 간에 물품거래와 배송 등 이행과 관련하여 필요한 범위 내에서 회원의 개인정보를 판매자에게 제공할 수 있습니다.

제9조     (접속정보의 관리)
1.	접속정보에 대한 관리책임은 회원에게 있으며, 회원은 어떠한 경우에도 본인의 접속정보를 타인에게 양도하거나 대여할 수 없습니다.
2.	회사의 귀책사유 없이 접속정보의 유출, 양도, 대여로 인하여 발생하는 손실이나 손해에 대하여는 회원 및 이용자 본인이 그에 대한 책임을 부담합니다.
3.	회원은 접속정보를 도난당하거나 제3자가 무단으로 이를 사용하고 있음을 인지한 경우, 이를 즉시 회사에 통보하여야 하고 회사는 이에 대한 신속한 처리를 위하여 최선의 노력을 다합니다.

제10조  (계약기간 및 이용계약의 종료)
1.	회원의 해지
(1)	회원은 언제든지 해당 서비스화면을 통하여 회사에게 해지의사를 통지함으로써 이용계약을 해지할 수 있습니다. 다만, 회원은 해지의사를 통지하기 최소한 7일 전에 모든 거래를 완결시키는데 필요한 거래완료, 철회 취소 등의 조치를 취하여야만 하고, 판매회원 또는 회사에 대한 미지급채무를 모두 완제해야 합니다.
(2)	전호의 기간 내에 회원이 발한 의사표시로 인해 발생한 불이익에 대한 책임은 회원 본인이 부담하여야 하며, 이용계약이 종료되면 회사는 회원에게 부가적으로 제공한 각종 혜택을 회수할 수 있습니다.
(3)	회원의 의사로 이용계약을 해지한 후, 추후 재이용을 희망할 경우에는 자유롭게 재이용이 가능합니다.
2.	회사의 해지
(1)	회사는 다음과 같은 사유가 발생하거나 확인된 경우 이용계약을 해지할 수 있습니다.
①	 다른 회원, 브랜드사회원, 인플런서(셀리) 또는 회사(직원 및 상담원 포함)를 포함한 타인의 지적재산권 등 재산상 권리나 명예, 신용 기타 인격적 권리(이하 합하여 “권리”라고 합니다)을 침해하거나 대한민국 법령 또는 공서양속에 위배되는 행위를 한 경우
②	회사가 제공하는 서비스의 원활한 진행을 방해하는 행위를 하거나 시도한 경우. 아래의 행위는 이에 해당하는 것으로 봅니다.
‐   합리적 사유 없이 상습적•악의적으로 이의를 제기하거나 비방하는 행위
‐   구매한 상품에 특별한 하자가 없음에도, 상습적으로 취소, 환불 요청 등을 하는 행위
‐   그 외 회사가 정한 안전거래 이용규칙을 위반한 경우
③	제7조 제5항 승낙거부사유가 있음이 확인된 경우
④	회원이 이 약관을 위반한 경우
⑤	기타 회사가 합리적인 판단에 기하여 서비스의 제공을 거부할 필요가 있다고 인정할 경우
(2)	회사가 해지를 하는 경우 회사는 회원에게 접속정보를 통하여 해지사유를 밝혀 해지의사를 통지합니다. 거래의 안전을 위하여 회사는 동일인 소유로 확인되는 회원의 접속정보(이용자 코드 포함)에 대해서 일괄적으로 해지를 할 수 있습니다. 이용계약은 회사의 해지의사를 회원에게 통지한 시점에 종료됩니다. 
(3)	본 항에 의하여 회사가 이용계약을 해지하더라도, 해지 이전에 이미 체결된 매매계약의 완결과 관련해서는 이 약관이 계속 적용됩니다.
(4)	본 항에서 정한 바에 따라 이용계약이 종료될 시에는 회사는 회원에게 부가적으로 제공한 각종 혜택을 회수할 수 있습니다. 
(5)	본 항에서 정한 바에 따라 이용계약이 종료된 경우에는, 회원의 재이용신청에 대하여 회사는 이에 대한 승낙을 거절할 수 있습니다.
(6)	회원에게 제7조 제5항의 이용신청 거부 사유가 있는지 판단하기 위해서 회사는 회원탈퇴 후 12개월 동안 해당 회원의 회원가입 기재 정보 및 서비스 이용 정보 등 개인정보를 보관하고, 그 이후에는 이를 삭제/폐기 처리합니다.
(7)	이용계약의 종료와 관련하여 발생한 손해에 대해서는 이용계약이 종료된 해당 회원이 책임을 져야 하고, 회사는 일절 책임을 지지 않습니다. 본 항에서 정한 바에 따라 이용계약이 종료되어 회사에 손해가 발생한 경우 해당 회원은 회사의 손해를 배상해야 합니다.

제11조  (서비스기간과 중단)
1.	이 약관에 따른 서비스 기간은 서비스 신청일로부터 이용계약의 해지 시까지입니다.
2.	회사는 컴퓨터 등 정보통신설비의 보수, 점검, 교체 및 고장, 통신의 두절 등의 사유가 발생한 경우에는 서비스의 제공을 일시적으로 중단할 수 있습니다. 이 경우 서비스 일시 중단 사실과 그 사유를 “쉘위샵” 초기화면에 통지합니다. 
3.	회사는 천재지변 또는 이에 준하는 불가항력으로 인하여 서비스를 제공할 수 없는 경우에는 서비스의 제공을 제한하거나 일시 중단할 수 있습니다.

제3장 구매서비스의 이용

제12조  (매매계약의 체결 및 대금 결제)
1.	상품의 매매계약은 회원이 판매자가 제시한 상품의 판매 조건에 응하여 청약의 의사표시를 하고 이에 대하여 판매자가 승낙의 의사표시를 함으로써 체결됩니다. 회원은 상품을 구매하기 전에 반드시 “쉘위샵”에 게시된 상품의 상세 내용과 거래의 조건을 정확하게 확인해야 합니다. 구매하려는 상품의 내용과 거래의 조건을 확인하지 않고 구매하여 발생한 모든 손실과 손해는 회원 본인에게 있습니다.
2.	회사는 회원이 현금, 카드 기타의 방법으로 매매 대금을 결제할 수 있는 방법을 제공합니다.
3.	매매 대금의 결제와 관련하여 회원이 입력한 정보 및 그 정보와 관련하여 발생한 책임과 불이익은 전적으로 구매자가 부담하여야 합니다. 
4.	상품을 주문한 후 주문일 다음 날 24시까지(주말 및 공휴일 포함) 매매대금을 결제하지 않을 경우 회사는 당해 주문을 주문취소로 간주하며, 회원의 동의 없이 취소할 수 있습니다. 
5.	회사는 구매자의 상품 매매계약 체결 내용을 ‘마이페이지’ 항목을 통해 확인할 수 있도록 조치하며, 매매계약의 취소 방법 및 절차를 안내합니다.
6.	회사는 구매자가 매매대금 결제 시 사용한 결제수단에 대해 정당한 사용권한을 가지고 있는지의 여부를 확인할 수 있으며, 이에 대한 확인이 완료될 때까지 거래진행을 중지하거나, 확인이 불가한 해당거래를 취소할 수 있습니다. 
7.	구매자가 실제로 결제하는 금액은 판매자가 정한 공급원가, 기본이용료, 상품에 적용된 할인쿠폰, 배송비, 옵션상품의 옵션내역 등이 적용된 금액(실구매액)이며 구매자에게 발행되는 구매증빙서류(현금영수증 및/또는 신용카드매출전표)는 실구매액으로 발행됩니다.

제13조 (배송)
1.	배송 소요기간은 입금 또는 대금결제 확인일의 익일을 기산일로 하여 배송이 완료되기까지의 기간(공휴일 및 휴무일 제외)을 말합니다. 
2.	회사는 판매자에게 회사로부터 구매자의 입금 또는 대금결제에 대한 확인통지를 받은 후 3영업일 이내에 배송에 필요한 조치를 취하도록 안내합니다. 단, 판매자가 상품 소개 페이지에 배송예정일을 명시한 경우 판매자는 배송예정일로부터 3일 내에 배송이 완료되도록 하면 됩니다.
3.	회사는 판매자가 구매자의 입금 또는 대금결제에 대한 확인통지를 받은 후 3영업일 이내에 배송에 필요한 조치를 하지 않을 경우 판매자가 판매하고 있는 상품에 대해 일괄 품절 처리를 할 수 있다.
4.	천재지변 등 불가항력적인 사유가 발생한 경우 그 해당기간은 배송 소요기간에서 제외됩니다. 
5.	회사는 배송과 관련하여 판매자와 구매자, 배송업체, 금융기관 등과의 사이에 발생한 분쟁은 당사자들 간의 해결을 원칙으로 하며, 회사는 어떠한 책임도 부담하지 않습니다.
6.	판매자의 발송확인 처리 이후에 구매자가 구매확정을 하지 않고 배송업체로부터 배송완료 정보를 받지 못하여 배송중 상태가 지연될 경우, 회사는 발송확인일로부터 [7일] 이내에 구매자에게 구매확정요청 안내를 할 수 있습니다. 회사의 안내 이후에도 구매자의 구매확정이 이루어지지 않을 경우 [3일]이 경과한 시점에 자동으로 배송완료로 전환될 수 있으며 이 경우 실제 구매자가 상품을 수령하지 못한 경우에 구매자는 미수취신고를 할 수 있습니다.
7.	회사는 구매자가 상품을 배송받고(전 항에 따라 배송완료 간주된 경우 포함) 구매확정 또는 구매확정으로 간주되는 경우 판매회원약관에서 정한 바에 따라 판매자에게 결제대금을 지급할 수 있습니다. 다만, 회사가 판매자에게 결제대금을 지급하기 이전에 구매자가 취소, 반품, 교환 또는 환불의 의사를 표시한 경우에는 그 지급을 보류합니다.
8.	판매자에게 결제대금 지급 후 청약철회, 취소, 해제, 무효 등의 사유가 발생한다면 회원은 판매자와 직접 청약철회, 취소, 해제 및 결제대금 환불 등에 관한 절차를 진행해야 하며, 회사는 이에 일체 책임이 없습니다.

제14조  (취소)
1.	회원은 구매한 상품이 발송되기 전까지 구매를 취소할 수 있으며, 배송중인 경우에는 취소가 아닌 반품절차에 따라 처리됩니다. 회원은 취소나 반품의 원인이 발생하면 수령한 상품을 임의로 사용하거나 훼손되도록 방치해서는 안 됩니다. 취소 또는 반품 상품의 임의사용이나 상품 보관 미흡으로 상품이 훼손되었을 때에는 그에 합당한 비용을 부담하여야 합니다.
2.	회원은 판매자와 상품매매 절차에서 분쟁이 발생하면 분쟁의 해결을 위하여 성실히 임해야 하며, 분쟁해결의 불성실로 판매자와 회사에 손실과 손해가 발생하면 그에 대한 모든 책임을 져야 합니다.
3.	회원이 결제를 완료한 후 배송준비중 상태에서는 취소신청 접수 시 특별한 사정이 없는 한 즉시 취소처리가 완료됩니다.
4.	회사가 회원의 구매대금 결제 확인에 대해 통지한 후 판매자가 상당 기간 회원의 주문 정보를 확인하지 않거나 배송 절차를 이행하지 않아 회원이 거래 취소를 요구하면 회사는 해당 거래를 취소할 수 있으며, 거래가 취소되면 보관 중인 상품 구매대금은 회원에게 환불됩니다. 아울러, 회사는 개별적인 정책으로 회원의 요구 없이도 자동 환불처리 등의 조치를 할 수 있으며 그럴 경우 미리 해당 내용을 회원에게 공지합니다.
5.	배송준비 상태에서 취소신청한 때에 이미 상품이 발송이 된 경우에는 발송된 상품의 왕복배송비를 구매자가 부담하는 것을 원칙으로 하며, 취소가 아닌 반품절차에 따라 처리됩니다.
6.	취소처리에 따른 환불은 카드결제의 경우 취소절차가 완료된 즉시 결제가 취소되며, 현금결제의 경우에는 3영업일 이내에 환불됩니다.

제15조  (반품)
1.	회원은 판매자의 상품 발송 시로부터 배송완료일 후 7일 이내까지 관계법령에 의거하여 반품을 신청할 수 있습니다.
2.	반품에 관한 일반적인 사항은 전자상거래 등에서의 소비자보호에 관한 법률 등 관련법령이 판매자가 제시한 조건보다 우선합니다.
3.	반품에 소요되는 비용은 반품에 대한 귀책사유가 있는 자에게 일반적으로 귀속됩니다. (예를 들어, 단순변심: 구매자부담, 상품하자: 판매자부담 등) 
4.	반품 신청 시 반품송장번호를 미기재하거나 반품사유에 관하여 판매자에게 정확히 통보(구두 또는 서면으로)하지 않을 시 반품처리 및 환불이 지연될 수 있습니다. 
5.	반품에 따른 환불은 반품 상품이 판매자에게 도착되고 반품사유 및 반품배송비 부담자가 확인된 이후에 현금결제의 경우에는 3영업일 이내에 환불되고, 카드 결제의 경우 즉시 결제가 취소됩니다.
6.	반품배송비를 구매자가 부담하여야 하는 경우 반품배송비의 추가 결제가 이루어지지 않으면 환불이 지연될 수 있습니다.
7.	회사는 회원의 교환 또는 반품의 요청이 정당하지 않다고 판단하면 회원의 의사표시를 취소하여 보관 중인 결제대금을 판매자에게 지급할 수 있습니다. 또한, 회원의 교환, 반품 등이 접수된 날로부터 14일이 지날 때까지 이미 수령한 상품을 판매자에게 반환하지 않거나 전화, 접속정보 등으로 연락되지 않으면 교환, 반품의 의사를 철회한 것으로 간주하여 보관 중인 결제대금을 판매자에게 지급할 수 있습니다.

제16조  (교환)
1.	회원은 판매자의 상품 발송 시로부터 배송완료일 후 7일이내까지 관계법령에 의거하여 교환을 신청할 수 있습니다.
2.	교환신청을 하더라도 판매자에게 교환할 물품의 재고가 없는 경우에는 교환이 불가능하며, 이 경우에 해당 교환신청은 반품으로 처리됩니다. 
3.	교환에 소요되는 비용은 물품하자 등 판매자의 원인으로 교환되는 경우에는 판매자가 왕복배송비를 부담하나 구매자의 변심 등 구매자의 원인으로 교환되는 경우에는 구매자가 부담합니다.

제17조  (환불)
1.	회사는 구매자의 취소 또는 반품에 의하여 환불사유가 발생할 시 현금결제의 경우에는 3영업일 이내에 환불하고 카드결제의 경우에는 즉시 결제가 취소됩니다.
2.	카드결제를 통한 구매건의 환불은 원칙적으로 카드결제 취소를 통해서만 가능합니다. 
3.	회원이 환불된 금액을 은행계좌로 입금 받고자 하는 경우에는 별도로 지정한 자신의 출금계좌로 현금출금요청을 할 수 있습니다.

제18조  (반품/교환/환불의 적용 배제)
1.	구매자는 다음 각 호의 경우에는 반품, 환불 또는 교환을 요청할 수 없습니다. 
(1)	구매자의 귀책사유로 말미암아 상품이 멸실•훼손된 경우
(2)	구매자의 사용 또는 일부 소비에 의하여 상품의 가치가 현저히 감소한 경우
(3)	시간의 경과에 의하여 재판매가 곤란할 정도로 상품의 가치가 현저히 감소한 경우
(4)	복제가 가능한 상품의 포장을 훼손한 경우
(5)	주문에 따라 개별적으로 생산되는 물품 등 판매자에게 회복할 수 없는 중대한 피해가 예상되는 경우로서 사전에 해당 거래에 대하여 별도로 그 사실을 고지하고 구매자의 서면(팝업창이나 체크박스에 동의가 표시되는 등 전자적 형태의 청약의 의사표시에 동의의 의사가 표시된 경우 포함)에 의한 동의를 받은 경우

제19조  (용역거래에 관한 특칙)
1.	여행상품, 숙박상품, 공연상품 등 재화 외의 용역(이하 “용역”)의 거래가 이루어지는 경우, 제13조의 규정에도 불구하고, 용역제공의 예약이 확정되는 경우(예컨대, 가능한 여행일정에 대한 확약, 가능한 객실에 대한 확약이 제공되는 경우)에 배송이 완료된 것으로 봅니다.
2.	용역의 거래가 이루어지는 경우, 취소, 반품 또는 환불기준은 판매자가 상품설명 화면에서 별도로 공지한 기준이 제14조 내지 제16조 및 제18조의 규정보다 우선 적용됩니다. 회원은 판매회원이 상품설명 화면에 공지한 취소, 반품 또는 환불기준을 숙지하여야 하며, 회원이 해당 용역을 구매하는 경우에는 판매자가 별도로 공지한 취소, 반품 또는 환불기준에 동의한 것으로 봅니다. 단, 판매자가 공지한 취소, 반품 또는 환불가능기간 및 조건이 『전자상거래의 소비자보호에 관한 법률』 또는 『소비자 분쟁해결기준』 등 관련법령보다 회원에게 불리한 경우에는 관련법령에 따라 취소, 반품 또는 환불가능기간 및 조건이 적용됩니다.
3.	제2항에 따라 취소, 반품 등으로 회원에게 환불이 이루어지는 경우, 환불방법은 제17조에서 정한 방법을 준용합니다.

제20조  (구매안전 서비스)
1.	회사는 e커머스 서비스의 신뢰성을 높이고 회원 간에 안전한 거래가 이루어질 수 있도록 전자상거래 등에서의 소비자보호에 관한 법률에 따라 구매안전 서비스를 제공합니다. 구매안전 서비스는 회원 간에 이루어지는 상품매매의 안전성과 신뢰성을 높이고, 상품 및 용역을 받기 전에 대금을 결제해야 하는 회원을 보호하기 위해 제공됩니다.
2.	구매안전 서비스란 상품매매계약이 체결되고 회원이 대금을 결제하였을 경우, 회원이 판매자로부터 해당 상품을 받았다면 회사는 미리 정한 서비스 이용료를 공제한 후 해당 판매자에게 대금을 정산해주고, 회원이 판매자로부터 상품을 받지 못했거나 받았더라도 판매자에게 반품한 후 환불을 요청했을 때에는 회원이 결제한 금액을 회사가 회원에게 환불해 주는 것을 말합니다.
3.	회사에서 제공하는 결제대금 보호 서비스는 판매자나 구매자를 대리하는 것이 아니며, 상품의 매매와 관련하여 판매자나 구매자의 의무를 대행하는 것도 아닙니다.
4.	구매안전 서비스를 통하여 상품구매대금을 결제하는 과정에서 발생하는 송금수수료 등은 대금을 송금하는 회원이 부담해야 합니다.
5.	구매안전 서비스를 제공하는 과정에서 이자 등이 발생했을 때 회원은 그 반환을 청구할 수 없습니다.
6.	회사는 구매자에게 상품을 공급받은 사실을 구매확정기간 내에 통보하여 주도록 요청하고, 회원은 구매결정 또는 취소, 반품, 교환 또는 환불의 의사표시를 해야 합니다.
7.	회사는 구매자가 구매확정 의사를 표시하거나 구매확정으로 간주가 되는 경우, 판매이용약관에서 정한 바에 따라 판매자에게 결제대금을 지급할 수 있습니다. 다만, 회사가 판매자에게 결제대금을 지급하기 이전에 구매자가 취소, 반품, 교환 또는 환불의 의사를 표시한 경우에는 그 지급을 보류합니다.
8.	판매자에게 결제대금 지급 후 청약철회, 취소, 해제, 무효 등의 사유가 발생한다면 회원은 판매자와 직접 청약철회, 취소, 해제 및 결제대금 환불 등에 관한 절차를 진행해야 하며, 회사는 이에 일체 책임이 없습니다.

제21조  (할인쿠폰)
1.	회사, 판매자 또는 인플런서(셀리)는 구매서비스를 이용하는 회원에게 상품 구매 시 일정액 또는 일정비율을 할인 받을 수 있는 할인쿠폰(할인코드 형태 포함)을 발급할 수 있습니다.
2.	회원은 할인쿠폰을 회원 본인의 구매에 한해서만 사용할 수 있으며, 어떠한 경우에도 이를 타인에게 실질적으로 매매 또는 양도할 수 없습니다.
3.	할인쿠폰은 품목이나 금액에 따라 사용이 제한될 수 있으며, 유효기간(할인쿠폰에 기재되어 있음)이 지난 후에는 사용할 수 없습니다.
4.	할인쿠폰을 사용하여 상품을 구입한 후 취소나 반품으로 인하여 환불이 이루어진 경우에는 유효기간이 남아 있다면 사용할 수 있습니다. 만약 취소나 환불 시 유효기간이 경과한 할인쿠폰의 경우에는 쿠폰의 발행기간이 남아있다면 자동으로 유효기간이 3일 연장되어 반환되며, 해당 기간 안에 할인쿠폰을 사용할 수 있습니다. 단, 단순변심에 의한 구매취소 등 구매자의 원인으로 인한 취소, 반품, 환불 시 또는 쿠폰의 유효기간과 발행기간이 모두 경과한 경우,  라이브 기간 동안 한정하여 판매회원(또는 인플런서)로부터 제공받은 할인쿠폰을 사용한 구매의 취소, 반품, 환불의 경우에는 재사용이 불가능할 수 있습니다.

제4장        이용자 관리 및 보호

 제22조  (회원 관리)
1.	회사는 본 약관의 본지와 관련 법령 및 상거래의 일반원칙을 위반한 회원에 대하여 다음과 같은 조치를 할 수 있습니다.
(1)	회사가 부가적으로 제공한 혜택의 일부 또는 전부의 회수 
(2)	특정서비스 이용제한
(3)	이용계약의 해지 
(4)	손해배상의 청구
2.	회사가 전항 각 호에 정한 조치를 할 경우 회사는 사전에 회원에게 접속정보로 통보며, 회원의 연락이 두절되거나 긴급을 요하는 것과 같이 부득이한 경우 선조치 후 사후 통보할 수 있습니다. 
3.	회원은 본 조에 의한 회사의 조치에 대하여 항변의 사유가 있는 경우 이에 대하여 항변을 할 수 있습니다.

 제23조  (저작물 책임)
1.	회사는 서비스를 이용함에 있어 저작권자의 저작권 보호를 위한 정책을 수립하여 운영하며 회원은 회사의 저작권 정책을 준수하여야 합니다. 
2.	회원이 회사가 제공하는 서비스를 이용하면서 작성한 게시물(영상물 제외)의 저작권은 이를 작성한 회원 본인에게 있으며, 해당 게시물이 타인의 저작권을 침해하는 경우 그에 대한 책임은 회원 본인이 부담합니다. 
3.	회사는 게시물(영상물 포함)이 다음 각 호에 해당하는 경우 사전 통보 없이 해당 게시물을 삭제하거나 게시자에 대하여 특정서비스의 이용제한, 이용계약의 해지 등의 조치를 할 수 있습니다. 단, 게시물이 다음 각 호 중 하나에 해당하지 않는 한, 회사는 게시물에 구매한 물품에 대한 단순불만 등 판매자에게 불리한 내용이 포함되었다는 이유만으로 게시물을 삭제하지 않습니다.
(1)	대한민국의 법령을 위반하는 내용을 포함하는 경우
(2)	관계법령에 의거 판매가 금지된 불법제품 또는 음란물을 게시, 광고하는 경우 
(3)	허위 또는 과대광고의 내용을 포함하는 경우
(4)	타인의 권리나 명예, 신용 기타 정당한 이익을 침해하는 경우 
(5)	직거래 유도 또는 타 사이트의 링크를 게시하는 경우
